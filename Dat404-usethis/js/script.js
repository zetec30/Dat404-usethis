//facebook logo from black to blue on mouse over.
function mouseOver() {
    if (window.document.face.src = 'assets/13962.png') {
        window.document.face.src = 'assets/13962-onmouse.png';

    }
}

function mouseOut() {
    if (window.document.face.src = 'assets/13962-onmouse.png') {
        window.document.face.src = 'assets/13962.png';

    }
}


